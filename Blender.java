public class Blender {
    int speed;
    boolean fill;

    int GetSpeed() {
        return speed;
    }

    void Fill(boolean fill) {
        if (fill = false) {
            fill = true;
        } else {
            fill = true;
        }
    }

    void Empty(boolean fill) {
        if (fill = true) {
            fill = false;
        } else {
            fill = false;
        }
    }
}